// A small but legitimately useful demo workflow we generate during the AI tour.
// It introduces workflow variables, {{workflow.var}} interpolation, and passing
// one step's output into another with {{step_id.field}}.
export function buildDemoWorkflow(id: string, name: string): DesignerModel {
  const triggerId = "trig_manual_demo";
  const step1 = "step_greet";
  const step2 = "step_timestamp";
  const step3 = "step_done";

  return {
    id,
    name,
    version: "1",
    description:
      "A small demo workflow generated by the onboarding tour. It uses workflow variables and passes one step's timestamp into the next step.",
    variables: [
      {
        name: "greeting",
        type: "string",
        scope: "workflow",
        defaultValue: "Hello there",
        description: "The greeting used in the first log step. Change it any time without editing the steps.",
        persistState: false,
      },
      {
        name: "task",
        type: "string",
        scope: "workflow",
        defaultValue: "review my inbox",
        description: "A short label for what this run is supposed to do.",
        persistState: false,
      },
    ],
    triggers: [
      {
        id: triggerId,
        type: "manual",
        label: "Run manually",
        args: {},
        position: { x: 120, y: 200 },
      },
    ],
    nodes: [
      {
        id: step1,
        type: "local.tool",
        tool: "log",
        label: "Greet",
        args: { message: "{{workflow.greeting}} - starting {{workflow.task}}." },
        fallbackTo: "",
        position: { x: 420, y: 200 },
      },
      {
        id: step2,
        type: "local.tool",
        tool: "get_datetime",
        label: "Get Timestamp",
        args: { format: MANUAL_ONBOARDING_TIMESTAMP_FORMAT },
        fallbackTo: "",
        position: { x: 720, y: 200 },
      },
      {
        id: step3,
        type: "local.tool",
        tool: "log",
        label: "Wrap up",
        args: { message: "Finished {{workflow.task}} at {{step_timestamp.formatted}}." },
        fallbackTo: "",
        position: { x: 1020, y: 200 },
      },
    ],
    wires: [
      { from: triggerId, to: step1 },
      { from: step1, to: step2 },
      { from: step2, to: step3 },
    ],
  };
}

